def main() -> int:
    """Main entry-point.

    Returns:
        Exit code: ``0`` = all passed, ``1`` = any failed.
    """
    parser = build_arg_parser()
    args = parser.parse_args()

    # Capture a single start time so the log file name and the report header
    # are always consistent regardless of how long argument parsing takes.
    start_time = datetime.datetime.now()

    # Auto-generate log file name if not provided.
    log_file = args.log_file
    if log_file is None:
        log_file = f"iperf_ptp_test_{start_time.strftime('%Y%m%d_%H%M%S')}.log"

    log = Logger(log_file=log_file, verbose=args.verbose)

    agent = IperfPTPTestAgent(
        gm_port=args.gm_port,
        fol_port=args.fol_port,
        gm_ip=args.gm_ip,
        fol_ip=args.fol_ip,
        samples=args.samples,
        post_iperf_samples=args.post_iperf_samples,
        convergence_timeout=args.convergence_timeout,
        iperf_bandwidth=args.iperf_bandwidth,
        iperf_duration=args.iperf_duration,
        iperf_port=args.iperf_port,
        stop_ptp_after=args.stop_ptp,
        from_step=args.from_step,
        log=log,
    )

    try:
        exit_code = agent.run(start_time=start_time)
    finally:
        log.close()

    return exit_code


if __name__ == "__main__":
    sys.exit(main())
