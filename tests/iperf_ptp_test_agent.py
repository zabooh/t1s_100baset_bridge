    # ------------------------------------------------------------------
    # Step 9 — Stop iPerf
    # ------------------------------------------------------------------

    def test_step_9_stop_iperf(self) -> bool:
        """Stop iPerf on both boards.

        Sends ``iperf_stop`` to both boards.  If the firmware does not
        recognise the command or does not reply with a confirmation, a warning
        is logged but the step is treated as passed — the iPerf client will
        finish naturally when its ``-t`` duration expires.

        Returns:
            ``True`` always (stop failures are non-fatal; the timed client
            will terminate on its own).
        """
        step = "Step 9: Stop iPerf"
        self.log.info(f"\n--- {step} ---")

        detail_parts = []

        for label, ser in [("GM ", self.gm_ser), ("FOL", self.fol_ser)]:
            self.log.info(f"  [{label}] iperf_stop")
            try:
                resp = send_command(ser, "iperf_stop", self.cmd_timeout, self.log)
                if RE_IPERF_STOPPED.search(resp):
                    detail_parts.append(f"{label} iperf stopped")
                    self.log.info(f"  [{label}] ✓ iPerf stopped")
                else:
                    detail_parts.append(f"{label} iperf_stop sent (no explicit ack)")
                    self.log.info(
                        f"  [{label}] ⚠ No explicit stop confirmation. "
                        f"Response: {resp.strip()!r}"
                    )
            except Exception as exc:
                detail_parts.append(f"{label} iperf_stop WARNING ({exc})")
                self.log.info(f"  [{label}] ⚠ iperf_stop failed (continuing): {exc}")

        # Allow the network to settle before collecting post-iPerf samples.
        self.log.info("  Waiting 2 s for network to settle…")
        time.sleep(2)

        self._record(step, True, "; ".join(detail_parts))
        return True

