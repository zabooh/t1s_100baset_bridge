def parse_offset(response: str) -> Optional[int]:
    """Extract the offset value (in nanoseconds) from a ``ptp_offset`` response.

    Expected format: ``[PTP] offset=+45 ns  abs=45 ns``

    Args:
        response: Raw CLI response string.

    Returns:
        Integer offset in ns, or ``None`` if not found.
    """
    m = RE_PTP_OFFSET.search(response)
    if m:
        return int(m.group(1))
    return None


def parse_ptp_status(response: str) -> Optional[Dict]:
    """Parse the output of ``ptp_status``.

    Expected format: ``[PTP] mode=master gmSyncs=198 gmState=3``

    Args:
        response: Raw CLI response string.

    Returns:
        Dict with keys ``mode``, ``gmSyncs`` (int), ``gmState`` (int), or
        ``None`` if the pattern was not found.
    """
    m = RE_PTP_STATUS.search(response)
    if m:
        return {
            "mode": m.group(1),
            "gmSyncs": int(m.group(2)),
            "gmState": int(m.group(3)),
        }
    return None


def _collect_offset_samples(
    fol_ser: serial.Serial,
    num_samples: int,
    cmd_timeout: float,
    log: Logger,
    label: str = "",
) -> List[int]:
    """Query ``ptp_offset`` *num_samples* times and return parsed values.

    Args:
        fol_ser: Open serial port to the Follower board.
        num_samples: How many offset queries to perform.
        cmd_timeout: Per-command timeout in seconds.
        log: :class:`Logger` for status output.
        label: Optional context label shown in debug messages.

    Returns:
        List of integer offset values (ns) that were successfully parsed.
    """
    offsets: List[int] = []
    prefix = f"[{label}] " if label else ""
    for i in range(num_samples):
        resp = send_command(fol_ser, "ptp_offset", cmd_timeout, log)
        value = parse_offset(resp)
        if value is not None:
            offsets.append(value)
            log.debug(f"  {prefix}sample {i+1}/{num_samples}: {value:+d} ns")
        else:
            log.info(
                f"  {prefix}✗ Could not parse offset from: {resp.strip()!r}"
            )
        time.sleep(0.2)
    return offsets


def _compute_offset_stats(offsets: List[int]) -> Dict:
    """Compute summary statistics for a list of offset values.

    Args:
        offsets: List of integer offset values in nanoseconds.

    Returns:
        Dict with keys ``samples``, ``mean``, ``stdev``, ``min``, ``max``,
        ``within``, and ``pct``.
    """
    mean_val = statistics.mean(offsets)
    stdev_val = statistics.stdev(offsets) if len(offsets) > 1 else 0.0
    min_val = min(offsets)
    max_val = max(offsets)
    within = [v for v in offsets if abs(v) <= OFFSET_THRESHOLD_NS]
    pct = 100.0 * len(within) / len(offsets)
    return {
        "samples": len(offsets),
        "mean": mean_val,
        "stdev": stdev_val,
        "min": min_val,
        "max": max_val,
        "within": len(within),
        "pct": pct,
    }


