class IperfPTPTestAgent:
    """Orchestrates the full iPerf+PTP test sequence.

    This agent starts a PTP Follower, launches an iPerf UDP server on the
    Follower board, an iPerf UDP client on the Grandmaster board, then starts
    the PTP Grandmaster.  It verifies that PTP converges to FINE state even
    under network load, validates offset accuracy during the load, stops
    iPerf, and validates continued stability afterwards.

    Args:
        gm_port: Serial port name for the Grandmaster board.
        fol_port: Serial port name for the Follower board.
        gm_ip: IP address to assign to the Grandmaster.
        fol_ip: IP address to assign to the Follower.
        netmask: Subnet mask (default ``255.255.255.0``).
        samples: Number of ``ptp_offset`` samples during iPerf load.
        post_iperf_samples: Number of ``ptp_offset`` samples after iPerf stops.
        convergence_timeout: Max seconds to wait for FINE state.
        cmd_timeout: Max seconds to wait for a single command response.
        iperf_bandwidth: iPerf target bandwidth string, e.g. ``"10M"``.
        iperf_duration: iPerf client run time in seconds.
        iperf_port: UDP port used by iPerf (default 5001).
        stop_ptp_after: If ``True`` (default), send ``ptp_mode off`` during cleanup.
        from_step: Skip steps with step number below this value.
        log: :class:`Logger` instance.
    """

    def __init__(
        self,
        gm_port: str,
        fol_port: str,
        gm_ip: str = DEFAULT_GM_IP,
        fol_ip: str = DEFAULT_FOL_IP,
        netmask: str = DEFAULT_NETMASK,
        samples: int = DEFAULT_SAMPLES,
        post_iperf_samples: int = DEFAULT_POST_IPERF_SAMPLES,
        convergence_timeout: float = DEFAULT_CONVERGENCE_TIMEOUT,
        cmd_timeout: float = DEFAULT_CMD_TIMEOUT,
        iperf_bandwidth: str = DEFAULT_IPERF_BANDWIDTH,
        iperf_duration: int = DEFAULT_IPERF_DURATION,
        iperf_port: int = DEFAULT_IPERF_PORT,
        stop_ptp_after: bool = True,
        from_step: int = 1,
        log: Logger = None,
    ):
        self.gm_port_name = gm_port
        self.fol_port_name = fol_port
        self.gm_ip = gm_ip
        self.fol_ip = fol_ip
        self.netmask = netmask
        self.samples = samples
        self.post_iperf_samples = post_iperf_samples
        self.convergence_timeout = convergence_timeout
        self.cmd_timeout = cmd_timeout
        self.iperf_bandwidth = iperf_bandwidth
        self.iperf_duration = iperf_duration
        self.iperf_port = iperf_port
        self.stop_ptp_after = stop_ptp_after
        self.from_step = from_step
        self.log = log or Logger()

        self.gm_ser: Optional[serial.Serial] = None
        self.fol_ser: Optional[serial.Serial] = None

        # Test results: (step_name, passed, detail)
        self.results: list = []
        # Offset statistics blocks populated by steps 8 and 10
        self._load_stats: Optional[Dict] = None
        self._post_iperf_stats: Optional[Dict] = None

    # ------------------------------------------------------------------
    # Connection management
    # ------------------------------------------------------------------

    def connect(self):
        """Open serial connections to both boards.

        Raises:
            SystemExit: On connection failure.
        """
        self.log.info(f"Connecting to GM  ({self.gm_port_name})…")
        try:
            self.gm_ser = open_port(self.gm_port_name)
            self.log.info(f"  GM  port open: {self.gm_port_name}")
        except serial.SerialException as exc:
            self.log.info(f"ERROR: Cannot open GM port {self.gm_port_name}: {exc}")
            sys.exit(1)

        self.log.info(f"Connecting to FOL ({self.fol_port_name})…")
        try:
            self.fol_ser = open_port(self.fol_port_name)
            self.log.info(f"  FOL port open: {self.fol_port_name}")
        except serial.SerialException as exc:
            self.log.info(f"ERROR: Cannot open FOL port {self.fol_port_name}: {exc}")
            if self.gm_ser:
                self.gm_ser.close()
            sys.exit(1)

    def disconnect(self):
        """Close serial ports gracefully."""
        for ser in (self.gm_ser, self.fol_ser):
            if ser and ser.is_open:
                try:
                    ser.close()
                except Exception:
                    pass

    # ------------------------------------------------------------------
    # Step 0 — Reset both boards
    # ------------------------------------------------------------------

    def test_step_0_reset(self):
        """Send a reset command to both boards and wait 8 seconds.

        Always executed regardless of ``--from-step``.  A failure to
        confirm the reset only produces a warning — the test continues.
        """
        step = "Step 0: Reset"
        self.log.info(f"\n--- {step} ---")

        detail_parts = []

        for label, ser in [("GM ", self.gm_ser), ("FOL", self.fol_ser)]:
            self.log.info(f"  [{label}] reset")
            try:
                resp = send_command(ser, "reset", self.cmd_timeout, self.log)
                detail_parts.append(f"{label} reset sent")
                self.log.info(f"  [{label}] ✓ reset command sent")
                if resp.strip():
                    self.log.debug(f"  [{label}] response: {resp.strip()!r}")
            except Exception as exc:
                detail_parts.append(f"{label} reset WARNING ({exc})")
                self.log.info(f"  [{label}] ⚠ reset failed (continuing): {exc}")

        self.log.info("  Waiting 8 s after reset…")
        time.sleep(8)

        self._record(step, True, "; ".join(detail_parts))

    # ------------------------------------------------------------------
    # Step 1 — IP configuration
    # ------------------------------------------------------------------

    def test_step_1_ip_config(self) -> bool:
        """Configure IP addresses on both boards.

        Sends ``setip eth0 <IP> <NETMASK>`` to each board and checks for the
        ``IP address set to`` confirmation message.

        Returns:
            ``True`` if both boards confirmed their IP address.
        """
        step = "Step 1: IP Configuration"
        self.log.info(f"\n--- {step} ---")

        passed = True
        detail_parts = []

        for label, ser, ip in [
            ("GM ", self.gm_ser, self.gm_ip),
            ("FOL", self.fol_ser, self.fol_ip),
        ]:
            cmd = f"setip eth0 {ip} {self.netmask}"
            self.log.info(f"  [{label}] {cmd}")
            resp = send_command(ser, cmd, self.cmd_timeout, self.log)
            if RE_IP_SET.search(resp):
                detail_parts.append(f"{label} IP ok ({ip})")
                self.log.info(f"  [{label}] ✓ IP set")
            else:
                detail_parts.append(f"{label} IP FAIL ({ip})")
                self.log.info(f"  [{label}] ✗ Unexpected response: {resp.strip()!r}")
                passed = False

        self._record(step, passed, "; ".join(detail_parts))
        return passed

    # ------------------------------------------------------------------
    # Step 2 — Network connectivity
    # ------------------------------------------------------------------

    def test_step_2_connectivity(self) -> bool:
        """Ping in both directions to verify network connectivity.

        Sends ``ping <IP>`` from each board to the other and checks for a
        ``Reply from`` confirmation.

        Returns:
            ``True`` if both pings replied.
        """
        step = "Step 2: Network Connectivity (GM→FOL, FOL→GM)"
        self.log.info(f"\n--- {step} ---")

        passed = True
        detail_parts = []

        for src_label, src_ser, dst_ip in [
            ("GM →FOL", self.gm_ser, self.fol_ip),
            ("FOL→GM ", self.fol_ser, self.gm_ip),
        ]:
            cmd = f"ping {dst_ip}"
            self.log.info(f"  [{src_label}] {cmd}")
            resp = send_command(src_ser, cmd, self.cmd_timeout, self.log)
            if RE_PING_REPLY.search(resp):
                detail_parts.append(f"{src_label} ok")
                self.log.info(f"  [{src_label}] ✓ Reply received")
            else:
                detail_parts.append(f"{src_label} FAIL")
                self.log.info(
                    f"  [{src_label}] ✗ No reply. Response: {resp.strip()!r}"
                )
                passed = False

        self._record(step, passed, "; ".join(detail_parts))
        return passed

    # ------------------------------------------------------------------
    # Step 3 — Start PTP Follower
    # ------------------------------------------------------------------

    def test_step_3_start_follower(self) -> bool:
        """Start PTP in Follower mode on the follower board.

        The follower must be running before the iPerf server is launched and
        before the Grandmaster starts.  This lets the follower begin listening
        for PTP packets as soon as the master appears.

        Returns:
            ``True`` if the follower confirmed ``[PTP] follower mode``.
        """
        step = "Step 3: Start PTP Follower"
        self.log.info(f"\n--- {step} ---")

        self.log.info("  [FOL] ptp_mode follower")
        resp = send_command(
            self.fol_ser, "ptp_mode follower", self.cmd_timeout, self.log
        )
        if RE_FOL_START.search(resp):
            detail = "FOL follower mode confirmed"
            self.log.info("  [FOL] ✓ follower mode confirmed")
            passed = True
        else:
            detail = f"FOL follower mode FAIL (resp={resp.strip()!r})"
            self.log.info(f"  [FOL] ✗ Unexpected response: {resp.strip()!r}")
            passed = False

        self._record(step, passed, detail)
        return passed

    # ------------------------------------------------------------------
    # Step 4 — Start iPerf server on Follower
    # ------------------------------------------------------------------

    def test_step_4_start_iperf_server(self) -> bool:
        """Start iPerf in server mode on the follower board.

        Sends ``iperf -s -u -p <port>`` to launch a UDP iPerf server.  Because
        the firmware command runs in the background (the board continues to
        respond to the CLI), a brief pause is included to allow the server to
        initialise before the client connects.

        If the firmware does not return a recognisable acknowledgement, a
        warning is logged but the step is still considered passed so that the
        test can continue — the client connection in Step 5 provides the real
        confirmation.

        Returns:
            ``True`` always (server-start errors are non-fatal; the client
            step acts as the functional check).
        """
        step = "Step 4: Start iPerf Server (FOL)"
        self.log.info(f"\n--- {step} ---")

        cmd = f"iperf -s -u -p {self.iperf_port}"
        self.log.info(f"  [FOL] {cmd}")
        resp = send_command(self.fol_ser, cmd, self.cmd_timeout, self.log)

        if RE_IPERF_SERVER_STARTED.search(resp):
            detail = f"iPerf server started on port {self.iperf_port}"
            self.log.info(f"  [FOL] ✓ {detail}")
        else:
            detail = (
                f"iPerf server cmd sent (port {self.iperf_port}); "
                f"no explicit confirmation — continuing"
            )
            self.log.info(
                f"  [FOL] ⚠ No explicit server-start confirmation. "
                f"Response: {resp.strip()!r}"
            )

        # Allow the server socket to open before the client connects.
        time.sleep(1)

        self._record(step, True, detail)
        return True

    # ------------------------------------------------------------------
    # Step 5 — Start iPerf client on GM
    # ------------------------------------------------------------------

    def test_step_5_start_iperf_client(self) -> bool:
        """Start iPerf in client mode on the Grandmaster board.

        Sends ``iperf -c <FOL_IP> -u -b <bandwidth> -t <duration>`` to
        generate a continuous UDP load towards the Follower for the configured
        duration.  The command is launched asynchronously (the firmware runs it
        in the background), so this step returns quickly.

        Returns:
            ``True`` if the client command was accepted (or if no explicit
            rejection was received).
        """
        step = "Step 5: Start iPerf Client (GM)"
        self.log.info(f"\n--- {step} ---")

        cmd = (
            f"iperf -c {self.fol_ip} -u "
            f"-b {self.iperf_bandwidth} "
            f"-t {self.iperf_duration} "
            f"-p {self.iperf_port}"
        )
        self.log.info(f"  [GM ] {cmd}")
        resp = send_command(self.gm_ser, cmd, self.cmd_timeout, self.log)

        if RE_IPERF_CLIENT_STARTED.search(resp):
            detail = (
                f"iPerf client started → {self.fol_ip}:{self.iperf_port} "
                f"bw={self.iperf_bandwidth} dur={self.iperf_duration}s"
            )
            self.log.info(f"  [GM ] ✓ {detail}")
            passed = True
        else:
            # The firmware may not echo a distinct confirmation; treat as
            # non-fatal to keep the test sequence going.
            detail = (
                f"iPerf client cmd sent → {self.fol_ip}:{self.iperf_port} "
                f"bw={self.iperf_bandwidth} dur={self.iperf_duration}s; "
                f"no explicit confirmation — continuing"
            )
            self.log.info(
                f"  [GM ] ⚠ No explicit client-start confirmation. "
                f"Response: {resp.strip()!r}"
            )
            passed = True  # non-fatal; PTP steps will reveal if traffic flows

        # Brief pause so traffic has time to establish before PTP starts.
        time.sleep(1)

        self._record(step, passed, detail)
        return passed

    # ------------------------------------------------------------------
    # Step 6 — Start PTP Grandmaster
    # ------------------------------------------------------------------

    def test_step_6_start_ptp_master(self) -> bool:
        """Start PTP in Grandmaster mode on the GM board.

        The grandmaster is started *after* iPerf traffic is already flowing so
        that convergence is tested under realistic network load conditions.

        Returns:
            ``True`` if the GM confirmed ``[PTP] grandmaster mode``.
        """
        step = "Step 6: Start PTP Grandmaster"
        self.log.info(f"\n--- {step} ---")

        self.log.info("  [GM ] ptp_mode master")
        resp = send_command(
            self.gm_ser, "ptp_mode master", self.cmd_timeout, self.log
        )
        if RE_GM_START.search(resp):
            detail = "GM grandmaster mode confirmed"
            self.log.info("  [GM ] ✓ grandmaster mode confirmed")
            passed = True
        else:
            detail = f"GM grandmaster mode FAIL (resp={resp.strip()!r})"
            self.log.info(f"  [GM ] ✗ Unexpected response: {resp.strip()!r}")
            passed = False

        self._record(step, passed, detail)
        return passed

    # ------------------------------------------------------------------
    # Step 7 — Convergence under iPerf load
    # ------------------------------------------------------------------

    def test_step_7_convergence_under_load(self) -> bool:
        """Monitor the Follower for convergence to FINE state while iPerf runs.

        Watches the Follower serial output for the state progression:
        ``UNINIT → MATCHFREQ → HARDSYNC → COARSE → FINE``

        The test succeeds only when ``PTP FINE`` appears within the configured
        timeout.  State-transition milestones are logged with their timestamps
        to aid debugging.

        Returns:
            ``True`` if FINE state was reached within ``convergence_timeout``.
        """
        step = "Step 7: Convergence to FINE (under iPerf load)"
        self.log.info(
            f"\n--- {step} (timeout={self.convergence_timeout}s) ---"
        )
        self.log.info(
            f"  iPerf load: bw={self.iperf_bandwidth}  "
            f"port={self.iperf_port}  dur={self.iperf_duration}s"
        )

        extra = {
            "MATCHFREQ": RE_MATCHFREQ,
            "HARD_SYNC": RE_HARD_SYNC,
            "COARSE": RE_COARSE,
        }

        matched, elapsed, milestones = wait_for_pattern(
            self.fol_ser,
            RE_FINE,
            timeout=self.convergence_timeout,
            log=self.log,
            extra_patterns=extra,
        )

        milestone_str = ", ".join(
            f"{k}@{v:.1f}s" for k, v in milestones.items()
        )
        if milestone_str:
            self.log.info(f"  Milestones: {milestone_str}")

        if matched:
            detail = (
                f"FINE reached in {elapsed:.1f}s under iPerf load "
                f"(bw={self.iperf_bandwidth})"
            )
            self.log.info(f"  ✓ {detail}")
        else:
            detail = (
                f"FINE NOT reached within {self.convergence_timeout}s "
                f"under iPerf load (milestones: {milestone_str or 'none'})"
            )
            self.log.info(f"  ✗ {detail}")

        self._record(step, matched, detail)
        return matched

