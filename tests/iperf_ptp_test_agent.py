#!/usr/bin/env python3
"""
iPerf PTP Test Agent for T1S 100BaseT Bridge
=============================================
Tests PTP (Precision Time Protocol) convergence and offset stability under
network load generated by iPerf UDP traffic.  Two ATSAME54P20A / LAN865x
boards connected via a 10BASE-T1S network are controlled through their serial
CLI interfaces.

Test Sequence
-------------
0. Reset both boards
1. Configure IP addresses (GM and Follower)
2. Verify bidirectional network connectivity (ping)
3. Start PTP Follower on the follower board
4. Start iPerf server on the follower board
5. Start iPerf client on the GM board (generates UDP load)
6. Start PTP Grandmaster on the GM board
7. Monitor convergence to FINE state while iPerf is running
8. Collect offset samples during iPerf load (validate ±100 ns threshold)
9. Stop iPerf on both boards
10. Verify PTP stability after iPerf stops (additional offset samples)
11. Cleanup (stop PTP on both boards, close ports)

Usage
-----
::

    # Basic run with default ports
    python tests/iperf_ptp_test_agent.py --gm-port COM10 --fol-port COM8

    # Custom bandwidth and duration
    python tests/iperf_ptp_test_agent.py --gm-port COM10 --fol-port COM8 \\
        --iperf-bandwidth 10M --iperf-duration 60

    # Verbose output with custom log file
    python tests/iperf_ptp_test_agent.py --gm-port COM10 --fol-port COM8 \\
        --verbose --log-file iperf_ptp_run1.log

    # Skip reset and IP config (resume from step 3)
    python tests/iperf_ptp_test_agent.py --gm-port COM10 --fol-port COM8 \\
        --from-step 3

Requirements
------------
::

    pip install pyserial
"""

